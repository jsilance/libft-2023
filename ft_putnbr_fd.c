#include "libft.h"

void	ft_putnbr_fd(int nbr, int fd)
{
	long long	nb;

	nb = nbr;
	if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		nb = -nb;
	}
	if (nb > 9)
	{
		ft_putnbr_fd(nb / 10, fd);
		nb %= 10;
	}
	if (nb <= 9)
		ft_putchar_fd('0' + nb, fd);
}
